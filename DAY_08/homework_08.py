#1번 문제
print("1.\n")

for i in range(5):
    print("*" * (i+1))

print("\n")


#2번 문제
print("2.\n")

for i in range(5):
    print(' ' * (5-i-1) + "*" * (i+1))

print("\n")



#3번 문제
print("3.\n")

for i in range(5):
    print("*" * (5-i))

print("\n")




#4번 문제
print("4.\n")

for i in range(5):
    print(' ' * (i) + "*" * (5-i))

print("\n")









